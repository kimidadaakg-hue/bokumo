def main() -> None:
    if not API_KEY:
        print("ERROR: GOOGLE_PLACES_API_KEY が設定されていません。", file=sys.stderr)
        sys.exit(1)

    total_points = len(SEARCH_POINTS)
    print(f"=== 北海道飲食店探索 ===")
    print(f"検索ポイント数: {total_points}")
    print(f"半径: {RADIUS}m / ポイント")
    print(f"出力先: {OUTPUT_PATH}")
    print()

    seen_ids: set[str] = set()
    results: list[dict] = []

    stats = {
        "total_raw": 0,
        "duplicates": 0,
        "chains": 0,
        "non_food": 0,
        "accepted": 0,
    }

    for idx, point in enumerate(SEARCH_POINTS, 1):
        label, lat, lng = point["name"], point["lat"], point["lng"]
        print(f"[{idx}/{total_points}] {label} ({lat}, {lng}) ...", end=" ")

        places = search_nearby(lat, lng)
        raw_count = len(places)
        stats["total_raw"] += raw_count

        point_accepted = 0
        for raw_place in places:
            parsed = parse_place(raw_place)
            if parsed is None:
                continue

            # 重複排除
            if parsed["place_id"] in seen_ids:
                stats["duplicates"] += 1
                continue
            seen_ids.add(parsed["place_id"])

            # チェーン店除外
            if is_chain(parsed["name"]):
                stats["chains"] += 1
                continue

            # 飲食タイプ判定
            if not is_food_place(parsed["types"]):
                stats["non_food"] += 1
                continue

            results.append(parsed)
            stats["accepted"] += 1
            point_accepted += 1

        print(f"API={raw_count}, 採用={point_accepted}")

        if idx < total_points:
            time.sleep(SLEEP_BETWEEN_CALLS)

    # 出力
    OUTPUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    with open(OUTPUT_PATH, "w", encoding="utf-8") as f:
        json.dump(results, f, ensure_ascii=False, indent=2)

    # サマリー
    print()
    print("=== 探索結果サマリー ===")
    print(f"検索ポイント数:    {total_points}")
    print(f"API 取得件数合計:  {stats['total_raw']}")
    print(f"重複除外:          {stats['duplicates']}")
    print(f"チェーン店除外:    {stats['chains']}")
    print(f"非飲食除外:        {stats['non_food']}")
    print(f"最終採用件数:      {stats['accepted']}")
    print(f"出力ファイル:      {OUTPUT_PATH}")


if __name__ == "__main__":
    main()
