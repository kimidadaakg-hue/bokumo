def is_chain(name: str) -> bool:
    """店名にチェーン店キーワードが含まれているか判定する。"""
    name_lower = name.lower()
    for kw in _CHAIN_KEYWORDS_LOWER:
        if kw in name_lower:
            return True
    return False


def is_food_place(types: list[str]) -> bool:
    """タイプリストに飲食関連タイプが一つでもあるか判定する。"""
    for t in types:
        if t in FOOD_TYPES:
            return True
    return False


def search_nearby(lat: float, lng: float) -> list[dict]:
    """Google Places API (New) Nearby Search を呼び出す。"""
    body = {
        "includedTypes": INCLUDED_TYPES,
        "maxResultCount": MAX_RESULT_COUNT,
        "languageCode": LANGUAGE_CODE,
        "regionCode": REGION_CODE,
        "locationRestriction": {
            "circle": {
                "center": {"latitude": lat, "longitude": lng},
                "radius": RADIUS,
            }
        },
    }

    data = json.dumps(body).encode("utf-8")

    req = urllib.request.Request(
        ENDPOINT,
        data=data,
        method="POST",
        headers={
            "Content-Type": "application/json",
            "X-Goog-Api-Key": API_KEY,
            "X-Goog-FieldMask": FIELD_MASK,
        },
    )

    try:
        with urllib.request.urlopen(req) as resp:
            result = json.loads(resp.read().decode("utf-8"))
    except urllib.error.HTTPError as e:
        body_text = e.read().decode("utf-8", errors="replace")
        print(f"  [ERROR] HTTP {e.code}: {body_text[:200]}", file=sys.stderr)
        return []
    except urllib.error.URLError as e:
        print(f"  [ERROR] URL error: {e.reason}", file=sys.stderr)
        return []

    return result.get("places", [])


def parse_place(raw: dict) -> dict | None:
    """API レスポンスの place オブジェクトを整形する。"""
    place_id = raw.get("id")
    display_name = raw.get("displayName", {})
    name = display_name.get("text", "")
    address = raw.get("formattedAddress", "")
    location = raw.get("location", {})
    lat = location.get("latitude")
    lng = location.get("longitude")
    primary_type = raw.get("primaryType", "")
    types = raw.get("types", [])

    if not place_id or not name:
        return None

    return {
        "place_id": place_id,
        "name": name,
        "address": address,
        "lat": lat,
        "lng": lng,
        "primaryType": primary_type,
        "types": types,
    }


